const filterItems = document.querySelectorAll(
  ".line-up-component-new__filter-item"
);
const lineupWrapper = document.querySelector(".line-up-wrapper");

function renderLineupFor(day) {
  lineupWrapper.classList.add("fade-out");

  setTimeout(() => {
    const dayData = lineupData[day];
    lineupWrapper.innerHTML = "";

    if (!dayData) {
      lineupWrapper.innerHTML = "<p>Line-up not available for this day.</p>";
    } else {
      dayData.forEach((stage) => {
        const lineupDiv = document.createElement("div");
        lineupDiv.className = `line-up theme-${stage.theme}`;

        const headerDiv = document.createElement("div");
        headerDiv.className = "line-up__header";

        const img = document.createElement("img");
        img.src = stage.image;
        img.alt = stage.alt;

        headerDiv.appendChild(img);
        lineupDiv.appendChild(headerDiv);

        stage.artists.forEach((artistName) => {
          const artistDiv = document.createElement("div");
          artistDiv.className = "line-up__artist";
          artistDiv.textContent = artistName;
          lineupDiv.appendChild(artistDiv);
        });

        lineupWrapper.appendChild(lineupDiv);
      });
    }

    lineupWrapper.classList.remove("fade-out");
  }, 400);
}

filterItems.forEach((item) => {
  item.addEventListener("click", (event) => {
    if (item.classList.contains("line-up-component-new__filter-item--active")) {
      return;
    }

    filterItems.forEach((i) =>
      i.classList.remove("line-up-component-new__filter-item--active")
    );

    const clickedItem = event.currentTarget;
    clickedItem.classList.add("line-up-component-new__filter-item--active");

    const selectedDay = clickedItem.dataset.day;
    renderLineupFor(selectedDay);
  });
});

document.addEventListener("DOMContentLoaded", () => {
  renderLineupFor("thursday");

  const toggleButton = document.getElementById("toggle-theme");
  const themeSelect = document.getElementById("theme-select");

  const applyTheme = (theme) => {
    if (theme === "light") {
      document.body.classList.add("light-theme");
    } else {
      document.body.classList.remove("light-theme");
    }

    if (themeSelect) {
      themeSelect.value = theme;
    }
  };

  if (toggleButton) {
    toggleButton.addEventListener("click", () => {
      const isLight = document.body.classList.toggle("light-theme");
      if (themeSelect) {
        themeSelect.value = isLight ? "light" : "default";
      }
    });
  }

  if (themeSelect) {
    themeSelect.addEventListener("change", () => {
      applyTheme(themeSelect.value);
    });
  }
});